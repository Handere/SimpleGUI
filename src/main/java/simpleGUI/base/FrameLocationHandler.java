package simpleGUI.base;

public class FrameLocationHandler {
    Frame frame;

    public FrameLocationHandler(Frame frame) {
        this.frame = frame;
    }

    public void left() {
        /**
         * Set the location of the frame to the left.
         */
    }
    public void right() {
        /**
         * Set the location of the frame to the right.
         */
    }
    public void top() {
        /**
         * Set the location of the frame to the top.
         */
    }
    public void bottom() {
        /**
         * Set the location of the frame to the bottom.
         */
    }
}
